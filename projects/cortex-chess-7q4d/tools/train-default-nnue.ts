// Offline trainer for the *shipped* NNUE network (dev-only; not part of the app
// build). It reuses the in-repo trainer (`nnue-train.ts`) and serializer
// (`nnue.ts`) exactly as the in-browser NNUE Lab does — same architecture, same
// knowledge-distillation objective (labels = the classical `evaluate`) — but runs
// to convergence offline so the result can be baked into the app. The output is
// `src/engine/nnue-weights.ts`, a base64 Float32 blob loaded as the default net so
// the "NNUE" toggle works on a fresh page load (no in-browser training required).
//
//   node tools/run-ts.mjs tools/train-default-nnue.ts [positions] [epochs]

import { writeFileSync } from 'node:fs'
import path from 'node:path'
import { generatePositions, NnueTrainer, correlation, type Example } from '../src/engine/nnue-train'
import { serializeNnue, DEFAULT_H } from '../src/engine/nnue'

const N = Number(process.argv[2] ?? 50000)
const EPOCHS = Number(process.argv[3] ?? 45)
const H = DEFAULT_H
const BATCH = 256

console.log(`Generating ${N} distilled positions…`)
const all: Example[] = generatePositions(N, 0x51ce)
const cut = Math.floor(all.length * 0.85)
const train = all.slice(0, cut)
const test = all.slice(cut)
console.log(`  ${train.length} train / ${test.length} holdout`)

const trainer = new NnueTrainer({ h: H, seed: 0x2025, lr: 1.5e-3 })

// Seeded Fisher–Yates shuffle each epoch.
let seed = 0x9e3779b9 >>> 0
const rnd = (): number => {
  seed = (seed + 0x6d2b79f5) | 0
  let t = Math.imul(seed ^ (seed >>> 15), 1 | seed)
  t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t
  return ((t ^ (t >>> 14)) >>> 0) / 4294967296
}

let finalLoss = 0
for (let e = 0; e < EPOCHS; e++) {
  for (let i = train.length - 1; i > 0; i--) {
    const j = Math.floor(rnd() * (i + 1))
    const tmp = train[i]
    train[i] = train[j]
    train[j] = tmp
  }
  let sse = 0
  let batches = 0
  for (let b = 0; b + BATCH <= train.length; b += BATCH) {
    sse += trainer.trainBatch(train.slice(b, b + BATCH))
    batches++
  }
  finalLoss = sse / Math.max(1, batches)
  if ((e + 1) % 5 === 0 || e === EPOCHS - 1) console.log(`  epoch ${e + 1}/${EPOCHS}  loss=${finalLoss.toFixed(6)}`)
}

const c = correlation(trainer.w, test)
console.log(`holdout: R²=${c.r2.toFixed(4)}  Pearson r=${c.r.toFixed(4)}  RMSE=${c.rmse.toFixed(1)}cp`)

// Pack the weights into one Float32 blob (w1, b1, w2, b2) and base64-encode it.
const blob = serializeNnue(trainer.w)
const floats = new Float32Array(blob.w1.length + blob.b1.length + blob.w2.length + 1)
let off = 0
floats.set(blob.w1, off); off += blob.w1.length
floats.set(blob.b1, off); off += blob.b1.length
floats.set(blob.w2, off); off += blob.w2.length
floats[off] = blob.b2
const bytes = new Uint8Array(floats.buffer)
let bin = ''
const CH = 0x8000
for (let i = 0; i < bytes.length; i += CH) bin += String.fromCharCode(...bytes.subarray(i, i + CH))
const b64 = btoa(bin)

const meta = {
  positions: train.length,
  epochs: EPOCHS,
  finalLoss: +finalLoss.toFixed(6),
  r2: +c.r2.toFixed(4),
  trainedAt: '2026-06-26',
}

const body = `// AUTO-GENERATED by tools/train-default-nnue.ts — do not edit by hand.
// The default NNUE network, trained offline to convergence by knowledge
// distillation of the classical evaluation (same architecture + objective as the
// in-browser NNUE Lab), stored as a base64-encoded Float32 blob. It is loaded as
// the default net so the "NNUE" toggle works on a fresh page load — a user who
// trains their own net in the Lab overrides it (their saved net wins).
import type { NnueBlob } from './nnue'
import type { NnueMeta } from './nnue-cache'

const H = ${H}
const DATA =
  '${b64}'

function decodeFloats(): Float32Array {
  const binStr = atob(DATA)
  const u8 = new Uint8Array(binStr.length)
  for (let i = 0; i < binStr.length; i++) u8[i] = binStr.charCodeAt(i)
  return new Float32Array(u8.buffer)
}

// Reconstruct the NnueBlob in exactly the shape serializeNnue() produces.
export function defaultNnueBlob(): NnueBlob {
  const a = decodeFloats()
  const n1 = 768 * H
  const n2 = H
  const n3 = 2 * H
  return {
    h: H,
    w1: Array.from(a.subarray(0, n1)),
    b1: Array.from(a.subarray(n1, n1 + n2)),
    w2: Array.from(a.subarray(n1 + n2, n1 + n2 + n3)),
    b2: a[n1 + n2 + n3],
  }
}

export const DEFAULT_NNUE_META: NnueMeta = ${JSON.stringify(meta, null, 2)}
`

const outPath = path.resolve(process.cwd(), 'src/engine/nnue-weights.ts')
writeFileSync(outPath, body)
console.log(`wrote ${outPath} (${(b64.length / 1024).toFixed(0)} KB base64)`)
