// The back end: lower the type-checked AST to textual RV32IM assembly that the studio's own
// two-pass assembler accepts. We use a textbook stack-machine discipline — every expression
// leaves its result in `a0`, binary operators spill the left operand to the stack — so the
// generated code is correct by construction (no register allocator to get wrong). Functions
// use a real RISC-V frame (saved `ra`/`fp`) and the standard a0–a7 + stack calling
// convention; globals, string literals, locals, pointers, arrays and structs all work.

import {
  decay,
  isPointer,
  isInteger,
  elementOf,
  type CType,
} from './ctype';
import { foldConst } from './parser';
import type {
  Program,
  FuncDecl,
  VarDecl,
  Stmt,
  Expr,
  BinOp,
  CallExpr,
} from './ast';

const A = ['a0', 'a1', 'a2', 'a3', 'a4', 'a5', 'a6', 'a7'];

// Builtins lowered inline to ecall / instruction sequences.
const SYSCALL_OF: Record<string, number> = {
  __sys_print_int: 1,
  __sys_print_uint: 36,
  __sys_print_char: 11,
  __sys_print_str: 4,
  __sys_sbrk: 9,
  __sys_exit: 93,
  __sys_rand: 41,
};

interface AnonStr {
  label: string;
  bytes: number[];
}

export class CodegenError extends Error {
  line: number;
  constructor(message: string, line: number) {
    super(message);
    this.line = line;
  }
}

export class Codegen {
  private out: string[] = [];
  private lbl = 0;
  private curFn: FuncDecl | null = null;
  private anonStrs: AnonStr[] = [];
  private prog: Program;
  private strings: readonly { value: string; label: string }[];

  constructor(prog: Program, strings: readonly { value: string; label: string }[]) {
    this.prog = prog;
    this.strings = strings;
  }

  generate(): string {
    this.emit('# Generated by the RISC-V Studio C compiler (cc).');
    this.emit('.text');
    this.emitStartup();
    for (const f of this.prog.funcs) {
      if (f.body) this.emitFunc(f);
    }
    this.emit('');
    this.emit('.data');
    this.emitData();
    return this.out.join('\n') + '\n';
  }

  // ---- emit helpers ----
  private emit(s: string): void {
    this.out.push(s);
  }
  private ins(s: string): void {
    this.out.push('        ' + s);
  }
  private mark(name: string): void {
    this.out.push(name + ':');
  }
  private uniq(base: string): string {
    return `.L.${base}.${this.lbl++}`;
  }

  // ---- startup: provide the assembler's `main` entry ----
  private emitStartup(): void {
    const main = this.prog.funcs.find((f) => f.name === 'main' && f.body);
    if (!main) throw new CodegenError("program has no 'main' function", 0);
    this.mark('main');
    this.ins('call _cc_main      # run C main()');
    this.ins('li   a7, 93        # exit2: a0 holds main() return value');
    this.ins('ecall');
    this.emit('');
  }

  // ---- functions ----
  private emitFunc(f: FuncDecl): void {
    this.curFn = f;
    const frame = f.frameSize;
    this.mark(f.sym!.label!);
    // Frame pointer (s0) relative layout, so big frames stay within 12-bit immediates:
    // ra at -4(s0), caller fp at -8(s0), locals below. s0 = caller's sp.
    this.ins('mv   t1, s0           # stash caller fp');
    if (frame <= 2047) {
      this.ins(`addi sp, sp, -${frame}`);
      this.ins(`addi s0, sp, ${frame}       # s0 = frame pointer`);
    } else {
      this.ins(`li   t0, ${frame}`);
      this.ins('sub  sp, sp, t0');
      this.ins('add  s0, sp, t0       # s0 = frame pointer');
    }
    this.ins('sw   ra, -4(s0)');
    this.ins('sw   t1, -8(s0)        # save caller fp');

    if (f.variadic) {
      // spill all argument registers into the save area for va_arg.
      for (let k = 0; k < 8; k++) this.storeFrame(A[k], f.vaBase! + k * 4);
    } else {
      f.params.forEach((p, i) => {
        if (i < 8) this.storeFrame(A[i], p.sym!.offset);
      });
    }

    this.genStmt(f.body!);

    this.mark(this.returnLabel(f));
    this.ins('lw   ra, -4(s0)');
    this.ins('lw   t0, -8(s0)        # caller fp');
    this.ins('mv   sp, s0            # pop frame');
    this.ins('mv   s0, t0');
    this.ins('ret');
    this.emit('');
    this.curFn = null;
  }

  private returnLabel(f: FuncDecl): string {
    return `.L.ret.${f.sym!.label}`;
  }

  // store `reg` to fp+offset, materialising big offsets safely.
  private storeFrame(reg: string, offset: number): void {
    if (offset >= -2048 && offset <= 2047) {
      this.ins(`sw   ${reg}, ${offset}(s0)`);
    } else {
      this.ins(`li   t2, ${offset}`);
      this.ins('add  t2, s0, t2');
      this.ins(`sw   ${reg}, 0(t2)`);
    }
  }
  // a0 <- address of fp+offset
  private frameAddr(offset: number): void {
    if (offset >= -2048 && offset <= 2047) {
      this.ins(`addi a0, s0, ${offset}`);
    } else {
      this.ins(`li   t2, ${offset}`);
      this.ins('add  a0, s0, t2');
    }
  }

  private push(): void {
    this.ins('addi sp, sp, -4');
    this.ins('sw   a0, 0(sp)');
  }
  private pop(reg: string): void {
    this.ins(`lw   ${reg}, 0(sp)`);
    this.ins('addi sp, sp, 4');
  }

  // ---- statements ----
  private genStmt(s: Stmt): void {
    switch (s.kind) {
      case 'block':
        for (const st of s.stmts) this.genStmt(st);
        break;
      case 'decl':
        for (const d of s.decls) this.genLocalInit(d);
        break;
      case 'expr':
        this.genExpr(s.expr);
        break;
      case 'if': {
        const els = this.uniq('else');
        const end = this.uniq('endif');
        this.genExpr(s.cond);
        this.ins(`beqz a0, ${s.els ? els : end}`);
        this.genStmt(s.then);
        if (s.els) {
          this.ins(`j    ${end}`);
          this.mark(els);
          this.genStmt(s.els);
        }
        this.mark(end);
        break;
      }
      case 'while': {
        const top = this.uniq('while');
        const end = this.uniq('endwhile');
        this.loopStack.push({ brk: end, cont: top });
        this.mark(top);
        this.genExpr(s.cond);
        this.ins(`beqz a0, ${end}`);
        this.genStmt(s.body);
        this.ins(`j    ${top}`);
        this.mark(end);
        this.loopStack.pop();
        break;
      }
      case 'dowhile': {
        const top = this.uniq('do');
        const cont = this.uniq('docont');
        const end = this.uniq('enddo');
        this.loopStack.push({ brk: end, cont });
        this.mark(top);
        this.genStmt(s.body);
        this.mark(cont);
        this.genExpr(s.cond);
        this.ins(`bnez a0, ${top}`);
        this.mark(end);
        this.loopStack.pop();
        break;
      }
      case 'for': {
        const top = this.uniq('for');
        const cont = this.uniq('forcont');
        const end = this.uniq('endfor');
        if (s.init) this.genStmt(s.init);
        this.loopStack.push({ brk: end, cont });
        this.mark(top);
        if (s.cond) {
          this.genExpr(s.cond);
          this.ins(`beqz a0, ${end}`);
        }
        this.genStmt(s.body);
        this.mark(cont);
        if (s.step) this.genExpr(s.step);
        this.ins(`j    ${top}`);
        this.mark(end);
        this.loopStack.pop();
        break;
      }
      case 'return':
        if (s.expr) this.genExpr(s.expr);
        this.ins(`j    ${this.returnLabel(this.curFn!)}`);
        break;
      case 'break':
        if (!this.loopStack.length) throw new CodegenError('break outside loop', s.line);
        this.ins(`j    ${this.loopStack[this.loopStack.length - 1].brk}`);
        break;
      case 'continue':
        if (!this.loopStack.length) throw new CodegenError('continue outside loop', s.line);
        this.ins(`j    ${this.loopStack[this.loopStack.length - 1].cont}`);
        break;
      case 'empty':
        break;
    }
  }

  private loopStack: { brk: string; cont: string }[] = [];

  private genLocalInit(d: VarDecl): void {
    if (!d.init) return;
    const t = d.type;
    if (t.kind === 'array' && t.base?.kind === 'char' && d.init.kind === 'str') {
      // char buf[] = "..."; copy the bytes into the local array.
      const bytes = strBytes(d.init.value);
      const n = t.len && t.len > 0 ? t.len : bytes.length;
      this.frameAddr(d.sym!.offset); // a0 = &buf
      this.ins('mv   t3, a0');
      for (let i = 0; i < n; i++) {
        const b = i < bytes.length ? bytes[i] : 0;
        this.ins(`li   t0, ${b}`);
        this.ins(`sb   t0, ${i}(t3)`);
      }
      return;
    }
    if (t.kind === 'struct' || t.kind === 'array') {
      throw new CodegenError('aggregate initializers are limited to char[] = "..."', d.line);
    }
    // scalar: addr = &var; value; store
    this.frameAddr(d.sym!.offset);
    this.push();
    this.genExpr(d.init);
    this.pop('t0');
    this.store(t, 't0');
  }

  // ---- expressions: value into a0 ----
  private genExpr(e: Expr): void {
    switch (e.kind) {
      case 'num':
        this.ins(`li   a0, ${e.value | 0}`);
        return;
      case 'str':
        this.ins(`la   a0, ${e.label}`);
        return;
      case 'ident':
        if (e.sym?.isFunc) {
          this.ins(`la   a0, ${e.sym.label}`);
          return;
        }
        this.genAddr(e);
        this.load(e.cty!);
        return;
      case 'index':
      case 'member':
        this.genAddr(e);
        this.load(e.cty!);
        return;
      case 'unary':
        this.genUnary(e);
        return;
      case 'binary':
        this.genBinary(e);
        return;
      case 'logical':
        this.genLogical(e);
        return;
      case 'assign':
        this.genAssign(e);
        return;
      case 'cond': {
        const els = this.uniq('celse');
        const end = this.uniq('cend');
        this.genExpr(e.cond);
        this.ins(`beqz a0, ${els}`);
        this.genExpr(e.then);
        this.ins(`j    ${end}`);
        this.mark(els);
        this.genExpr(e.els);
        this.mark(end);
        return;
      }
      case 'comma':
        this.genExpr(e.lhs);
        this.genExpr(e.rhs);
        return;
      case 'call':
        this.genCall(e);
        return;
      case 'cast':
        this.genExpr(e.operand);
        if (e.toType.kind === 'char') {
          this.ins('slli a0, a0, 24');
          this.ins('srai a0, a0, 24');
        }
        return;
      case 'sizeof': {
        const sz = e.argType ? e.argType.size : e.argExpr!.cty!.size;
        this.ins(`li   a0, ${sz}`);
        return;
      }
      case 'va_arg': {
        this.genAddr(e.ap);
        this.ins('mv   t0, a0'); // t0 = &ap
        this.ins('lw   a1, 0(t0)'); // a1 = current slot pointer
        this.ins('lw   a0, 0(a1)'); // a0 = arg value
        this.ins('addi a1, a1, 4');
        this.ins('sw   a1, 0(t0)'); // advance ap
        return;
      }
      case 'vactl': {
        if (e.which === 'start') {
          const f = this.curFn!;
          const numNamed = f.params.length;
          this.genAddr(e.ap);
          this.ins('mv   t0, a0');
          this.frameAddr(f.vaBase! + numNamed * 4); // a0 = &firstVararg
          this.ins('sw   a0, 0(t0)');
        }
        // va_end: no-op
        return;
      }
    }
  }

  // ---- address of an lvalue into a0 ----
  private genAddr(e: Expr): void {
    switch (e.kind) {
      case 'ident': {
        const sym = e.sym!;
        if (sym.storage === 'global') {
          this.ins(`la   a0, ${sym.label}`);
        } else {
          this.frameAddr(sym.offset);
        }
        return;
      }
      case 'str':
        this.ins(`la   a0, ${e.label}`);
        return;
      case 'unary':
        if (e.op === 'deref') {
          this.genExpr(e.operand); // value of pointer = address
          return;
        }
        break;
      case 'index': {
        // *(base + index)
        const bt = decay(e.base.cty!);
        const it = decay(e.index.cty!);
        let ptrExpr = e.base;
        let idxExpr = e.index;
        let ptrType = bt;
        if (!isPointer(bt) && isPointer(it)) {
          ptrExpr = e.index;
          idxExpr = e.base;
          ptrType = it;
        }
        const size = Math.max(elementOf(ptrType).size, 1);
        this.genExpr(idxExpr);
        if (size !== 1) {
          this.ins(`li   t2, ${size}`);
          this.ins('mul  a0, a0, t2');
        }
        this.push();
        this.genExpr(ptrExpr);
        this.pop('t1');
        this.ins('add  a0, a0, t1');
        return;
      }
      case 'member': {
        if (e.arrow) {
          this.genExpr(e.obj); // pointer value
        } else {
          this.genAddr(e.obj); // address of struct
        }
        if (e.offset! !== 0) this.ins(`addi a0, a0, ${e.offset}`);
        return;
      }
    }
    throw new CodegenError('expression is not an lvalue', e.line);
  }

  // load value at address in a0 into a0, per type
  private load(t: CType): void {
    if (t.kind === 'array' || t.kind === 'struct' || t.kind === 'func') return; // value is the address
    if (t.kind === 'char') this.ins('lb   a0, 0(a0)');
    else this.ins('lw   a0, 0(a0)'); // int / ptr
  }
  // store a0 to address in `addrReg`, per type
  private store(t: CType, addrReg: string): void {
    if (t.kind === 'char') this.ins(`sb   a0, 0(${addrReg})`);
    else if (t.kind === 'struct') this.copyStruct(addrReg, 'a0', t.size);
    else this.ins(`sw   a0, 0(${addrReg})`);
  }

  private copyStruct(dstReg: string, srcReg: string, size: number): void {
    // dst in dstReg, src address in srcReg; copy `size` bytes
    let i = 0;
    for (; i + 4 <= size; i += 4) {
      this.ins(`lw   t4, ${i}(${srcReg})`);
      this.ins(`sw   t4, ${i}(${dstReg})`);
    }
    for (; i < size; i++) {
      this.ins(`lb   t4, ${i}(${srcReg})`);
      this.ins(`sb   t4, ${i}(${dstReg})`);
    }
  }

  private genUnary(e: import('./ast').UnaryExpr): void {
    switch (e.op) {
      case 'addr':
        this.genAddr(e.operand);
        return;
      case 'deref':
        this.genExpr(e.operand);
        this.load(e.cty!);
        return;
      case 'neg':
        this.genExpr(e.operand);
        this.ins('neg  a0, a0');
        return;
      case 'pos':
        this.genExpr(e.operand);
        return;
      case 'bnot':
        this.genExpr(e.operand);
        this.ins('not  a0, a0');
        return;
      case 'not':
        this.genExpr(e.operand);
        this.ins('seqz a0, a0');
        return;
      case 'preinc':
      case 'predec':
      case 'postinc':
      case 'postdec': {
        const t = decay(e.operand.cty!);
        const delta = isPointer(t) ? Math.max(elementOf(t).size, 1) : 1;
        const dec = e.op === 'predec' || e.op === 'postdec';
        const post = e.op === 'postinc' || e.op === 'postdec';
        this.genAddr(e.operand);
        this.ins('mv   t0, a0'); // t0 = addr
        this.load(e.operand.cty!); // a0 = old value
        this.ins('mv   t1, a0'); // t1 = old
        this.ins(`${dec ? 'addi a0, a0, -' : 'addi a0, a0, '}${delta}`);
        this.store(e.operand.cty!, 't0');
        if (post) this.ins('mv   a0, t1'); // result = old
        return;
      }
    }
  }

  private genBinary(e: import('./ast').BinaryExpr): void {
    const lt = decay(e.lhs.cty!);
    const rt = decay(e.rhs.cty!);
    const op = e.op;

    // pointer arithmetic
    if ((op === '+' || op === '-') && isPointer(lt) && isInteger(rt)) {
      const size = Math.max(elementOf(lt).size, 1);
      this.genExpr(e.rhs);
      if (size !== 1) {
        this.ins(`li   t2, ${size}`);
        this.ins('mul  a0, a0, t2');
      }
      this.push();
      this.genExpr(e.lhs);
      this.pop('t1');
      this.ins(`${op === '+' ? 'add' : 'sub'}  a0, a0, t1`);
      return;
    }
    if (op === '+' && isInteger(lt) && isPointer(rt)) {
      const size = Math.max(elementOf(rt).size, 1);
      this.genExpr(e.lhs);
      if (size !== 1) {
        this.ins(`li   t2, ${size}`);
        this.ins('mul  a0, a0, t2');
      }
      this.push();
      this.genExpr(e.rhs);
      this.pop('t1');
      this.ins('add  a0, a0, t1');
      return;
    }
    if (op === '-' && isPointer(lt) && isPointer(rt)) {
      const size = Math.max(elementOf(lt).size, 1);
      this.genExpr(e.rhs);
      this.push();
      this.genExpr(e.lhs);
      this.pop('t1');
      this.ins('sub  a0, a0, t1');
      if (size !== 1) {
        this.ins(`li   t2, ${size}`);
        this.ins('div  a0, a0, t2');
      }
      return;
    }

    // plain integer (and pointer comparisons)
    this.genExpr(e.rhs);
    this.push();
    this.genExpr(e.lhs);
    this.pop('t1');
    this.emitArith(op);
  }

  private emitArith(op: BinOp): void {
    switch (op) {
      case '+':
        this.ins('add  a0, a0, t1');
        break;
      case '-':
        this.ins('sub  a0, a0, t1');
        break;
      case '*':
        this.ins('mul  a0, a0, t1');
        break;
      case '/':
        this.ins('div  a0, a0, t1');
        break;
      case '%':
        this.ins('rem  a0, a0, t1');
        break;
      case '<<':
        this.ins('sll  a0, a0, t1');
        break;
      case '>>':
        this.ins('sra  a0, a0, t1');
        break;
      case '&':
        this.ins('and  a0, a0, t1');
        break;
      case '|':
        this.ins('or   a0, a0, t1');
        break;
      case '^':
        this.ins('xor  a0, a0, t1');
        break;
      case '<':
        this.ins('slt  a0, a0, t1');
        break;
      case '>':
        this.ins('slt  a0, t1, a0');
        break;
      case '<=':
        this.ins('slt  a0, t1, a0');
        this.ins('xori a0, a0, 1');
        break;
      case '>=':
        this.ins('slt  a0, a0, t1');
        this.ins('xori a0, a0, 1');
        break;
      case '==':
        this.ins('sub  a0, a0, t1');
        this.ins('seqz a0, a0');
        break;
      case '!=':
        this.ins('sub  a0, a0, t1');
        this.ins('snez a0, a0');
        break;
    }
  }

  private genLogical(e: import('./ast').LogicalExpr): void {
    if (e.op === '&&') {
      const f = this.uniq('andfalse');
      const end = this.uniq('andend');
      this.genExpr(e.lhs);
      this.ins(`beqz a0, ${f}`);
      this.genExpr(e.rhs);
      this.ins(`beqz a0, ${f}`);
      this.ins('li   a0, 1');
      this.ins(`j    ${end}`);
      this.mark(f);
      this.ins('li   a0, 0');
      this.mark(end);
    } else {
      const t = this.uniq('ortrue');
      const end = this.uniq('orend');
      this.genExpr(e.lhs);
      this.ins(`bnez a0, ${t}`);
      this.genExpr(e.rhs);
      this.ins(`bnez a0, ${t}`);
      this.ins('li   a0, 0');
      this.ins(`j    ${end}`);
      this.mark(t);
      this.ins('li   a0, 1');
      this.mark(end);
    }
  }

  private genAssign(e: import('./ast').AssignExpr): void {
    const t = e.target.cty!;
    if (e.op === null) {
      this.genAddr(e.target);
      this.push();
      this.genExpr(e.value);
      this.pop('t0');
      this.store(t, 't0');
      return;
    }
    // compound: target op= value
    this.genAddr(e.target);
    this.push(); // save addr
    this.genAddr(e.target); // recompute addr to load current
    this.load(t);
    this.push(); // save current lhs value
    this.genExpr(e.value);
    this.ins('mv   t1, a0'); // t1 = rhs
    this.pop('a0'); // a0 = lhs value
    // pointer-scaled compound for += / -=
    const dt = decay(t);
    if ((e.op === '+' || e.op === '-') && isPointer(dt)) {
      const size = Math.max(elementOf(dt).size, 1);
      if (size !== 1) {
        this.ins(`li   t2, ${size}`);
        this.ins('mul  t1, t1, t2');
      }
    }
    this.emitArith(e.op);
    this.pop('t0'); // t0 = addr
    this.store(t, 't0');
  }

  private genCall(e: CallExpr): void {
    // builtin lowering
    if (e.callee.kind === 'ident' && e.callee.sym?.isFunc) {
      const name = e.callee.name;
      if (name === '__lsr') {
        this.genExpr(e.args[1]);
        this.push();
        this.genExpr(e.args[0]);
        this.pop('t1');
        this.ins('srl  a0, a0, t1');
        return;
      }
      if (name in SYSCALL_OF) {
        if (e.args.length === 1) this.genExpr(e.args[0]);
        this.ins(`li   a7, ${SYSCALL_OF[name]}`);
        this.ins('ecall');
        return;
      }
    }

    const n = e.args.length;
    const nstack = Math.max(0, n - 8);
    // evaluate args right-to-left, pushing each
    for (let i = n - 1; i >= 0; i--) {
      this.genExpr(e.args[i]);
      this.push();
    }
    // direct vs indirect call
    if (e.callee.kind === 'ident' && e.callee.sym?.isFunc) {
      for (let i = 0; i < Math.min(8, n); i++) this.pop(A[i]);
      this.ins(`call ${e.callee.sym.label}`);
    } else {
      this.genExpr(e.callee);
      this.ins('mv   t0, a0');
      for (let i = 0; i < Math.min(8, n); i++) this.pop(A[i]);
      this.ins('jalr t0');
    }
    if (nstack > 0) this.ins(`addi sp, sp, ${nstack * 4}`);
  }

  // ---- data section ----
  private emitData(): void {
    for (const g of this.prog.globals) this.emitGlobal(g);
    for (const s of this.strings) this.emitStringData(s.label, s.value);
    for (const a of this.anonStrs) this.emitByteData(a.label, a.bytes);
  }

  private emitGlobal(g: VarDecl): void {
    const label = g.sym!.label!;
    const t = g.type;
    if (!g.init) {
      this.mark(label);
      this.emit(`        .space ${Math.max(t.size, 1)}`);
      return;
    }
    if (t.kind === 'array' && t.base?.kind === 'char' && g.init.kind === 'str') {
      const bytes = strBytes(g.init.value);
      bytes.push(0);
      while (t.len && bytes.length < t.len) bytes.push(0);
      this.emitByteData(label, bytes);
      return;
    }
    if (isPointer(t) && g.init.kind === 'str') {
      const anon = `__cc_gstr_${this.anonStrs.length}`;
      const bytes = strBytes(g.init.value);
      bytes.push(0);
      this.anonStrs.push({ label: anon, bytes });
      this.mark(label);
      this.emit(`        .word ${anon}`);
      return;
    }
    const v = foldConst(g.init);
    if (v === null) throw new CodegenError(`global '${g.name}' needs a constant initializer`, g.line);
    this.mark(label);
    if (t.kind === 'char') this.emit(`        .byte ${v & 0xff}`);
    else this.emit(`        .word ${v | 0}`);
  }

  private emitStringData(label: string, value: string): void {
    const bytes = strBytes(value);
    bytes.push(0);
    this.emitByteData(label, bytes);
  }

  private emitByteData(label: string, bytes: number[]): void {
    this.mark(label);
    for (let i = 0; i < bytes.length; i += 16) {
      const chunk = bytes.slice(i, i + 16).map((b) => b & 0xff);
      this.emit('        .byte ' + chunk.join(', '));
    }
    if (bytes.length === 0) this.emit('        .byte 0');
  }
}

function strBytes(s: string): number[] {
  const out: number[] = [];
  for (let i = 0; i < s.length; i++) out.push(s.charCodeAt(i) & 0xff);
  return out;
}

export function generate(
  prog: Program,
  strings: readonly { value: string; label: string }[],
): string {
  return new Codegen(prog, strings).generate();
}
